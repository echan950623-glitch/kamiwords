def sql_escape(s: str) -> str:
    return s.replace("'", "''")


def render_value_row(row: dict) -> str:
    seq = int(row['jmdict_seq'])
    kana = row['kana']
    kanji = row['kanji']
    lemma = kanji if kanji else kana
    en = row['waller_definition']
    key = (seq, kana, kanji)
    if key not in TRANSLATIONS:
        raise KeyError(f"Missing translation for {key} ({lemma}, {en})")
    zh = TRANSLATIONS[key]
    meta = json.dumps(
        {'kana': kana, 'jlpt': 'N4', 'jmdict_seq': seq, 'meaning_en': en},
        ensure_ascii=False,
    )
    return (
        f"    ('ja', '{sql_escape(lemma)}', "
        f"'{sql_escape(zh)}', "
        f"'{sql_escape(meta)}'::jsonb, 'jmdict')"
    )


