    sql_005 = build_sql(
        '005_n5_words_meiji',
        f'N5 單字匯入：明治神宮（CSV 行 344-685，共 {len(meiji)} 字）',
        meiji,
        'meiji',
        position_offset=0,
    )

    p_004 = out_dir / '004_n5_words_inari_part2.sql'
    p_005 = out_dir / '005_n5_words_meiji.sql'
    p_004.write_text(sql_004, encoding='utf-8')
    p_005.write_text(sql_005, encoding='utf-8')

    print(f"Wrote {p_004} ({p_004.stat().st_size} bytes, {sql_004.count(chr(10)) + 1} lines)")
    print(f"Wrote {p_005} ({p_005.stat().st_size} bytes, {sql_005.count(chr(10)) + 1} lines)")


